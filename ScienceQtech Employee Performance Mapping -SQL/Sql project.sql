CREATE DATABASE employee;
USE employee;
CREATE TABLE emp_record_table (
    EMP_ID       VARCHAR(10) PRIMARY KEY,
    FIRST_NAME   VARCHAR(50),
    LAST_NAME    VARCHAR(50),
    GENDER       CHAR(1),
    ROLE         VARCHAR(100),
    DEPT         VARCHAR(50),
    EXP          INT,
    COUNTRY      VARCHAR(50),
    CONTINENT    VARCHAR(50),
    SALARY       INT,
    EMP_RATING   INT,
    MANAGER_ID   VARCHAR(10),
    PROJ_ID      VARCHAR(10)
);
CREATE TABLE proj_table (
    PROJECT_ID   VARCHAR(10) PRIMARY KEY,
    PROJ_NAME    VARCHAR(100),
    DOMAIN       VARCHAR(50),
    START_DATE   DATE,
    CLOSURE_DATE DATE,
    DEV_QTR      VARCHAR(10),
    STATUS       VARCHAR(20)
);
CREATE TABLE data_science_team (
    EMP_ID      VARCHAR(10) PRIMARY KEY,
    FIRST_NAME  VARCHAR(50),
    LAST_NAME   VARCHAR(50),
    GENDER      CHAR(1),
    ROLE        VARCHAR(100),
    DEPT        VARCHAR(50),
    EXP         INT,
    COUNTRY     VARCHAR(50),
    CONTINENT   VARCHAR(50)
);
SELECT * FROM emp_record_table;
SELECT * FROM proj_table;
SELECT e.EMP_ID, e.FIRST_NAME, e.LAST_NAME, p.PROJ_NAME, p.STATUS
FROM emp_record_table e
LEFT JOIN proj_table p
ON e.PROJ_ID = p.PROJECT_ID;
SELECT DEPT, COUNT(*) AS total_employees
FROM emp_record_table
GROUP BY DEPT;
SELECT DEPT, FIRST_NAME, LAST_NAME, SALARY
FROM emp_record_table
WHERE (DEPT, SALARY) IN (
    SELECT DEPT, MAX(SALARY)
    FROM emp_record_table
    GROUP BY DEPT
);
SELECT EMP_ID, FIRST_NAME, LAST_NAME
FROM emp_record_table
WHERE PROJ_ID IS NULL;
SELECT CONTINENT, AVG(SALARY) AS avg_salary
FROM emp_record_table
GROUP BY CONTINENT;
SELECT STATUS, COUNT(*) AS total_projects
FROM proj_table
GROUP BY STATUS;
SELECT d.EMP_ID, d.FIRST_NAME, d.ROLE, e.PROJ_ID
FROM data_science_team d
LEFT JOIN emp_record_table e
ON d.EMP_ID = e.EMP_ID;

